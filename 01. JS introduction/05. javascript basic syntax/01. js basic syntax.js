// js variable => In JavaScript, you can declare variables using the var, let, or const keywords.
var x = 5;      
var y = 10;     
var z = x + y;
console.log(z); 

// js data types = JavaScript supports several data types, including numbers, strings, booleans, arrays, objects, and more.
var num = 42;
var str = "Hello, world!";
var bool = true;
var arr = [1, 2, 3, 4];
var obj = {name: "John", age: 30};
console.log(num,str,bool,arr,obj);

// Operators => JavaScript supports a variety of operators for performing arithmetic, comparison, logical, and other operations.
var sum = 1 + 2;              // addition
var diff = 5 - 3;             // subtraction
var product = 2 * 4;          // multiplication
var quotient = 10 / 2;        // division
var remainder = 7 % 3;        // modulus
var greaterThan = 5 > 3;      // greater than
var lessThan = 3 < 5;         // less than
var equals = 4 == "4";        // equality (type coercion)
var strictEquals = 4 === "4"; // strict equality (no type coercion)
var and = true && false;      // logical AND
var or = true || false;       // logical OR
console.log(sum+' '+diff+' '+product+' '+quotient+' '+remainder+' '+greaterThan+' '+lessThan+' '+equals+' '+strictEquals+' '+and+' '+or);