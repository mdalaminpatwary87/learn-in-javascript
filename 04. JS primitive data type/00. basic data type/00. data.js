/*====================================================================================================================================================
JavaScript Data Types => 
                      -  JavaScript variables can hold numbers like 100 and text values like "John Doe".
                      -  In programming, text values are called text strings.
                      -  JavaScript can handle many types of data, but for now, just think of numbers and strings.
                      -  Strings are written inside double or single quotes. Numbers are written without quotes.
                      -  If you put a number in quotes, it will be treated as a text string.
====================================================================================================================================================*/
var a = 3; // number_decimal
var pi = 3.14; // number_float 
var person = "John Doe"; // string - using single quotes
var answer = 'Yes I am!'; // string - using double quotes
var answer = '3'; // Put a number in quotes, it will be treated as a text string.
console.log(a)
console.log(pi)
console.log(person)
console.log(answer)