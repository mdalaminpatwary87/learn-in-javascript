// Assignment operators assign values to JavaScript variables.
var x = 10;    // Assign the value 10 to x
var y = 5;     // Assign the value 5 to y
var z = x + y; // Assign the value x + y to z:
console.log(z)

// Addition Equal(+=)     
var x = 10;
x += 5; // same as x = x + 5
console.log(x)

// Subtract Equal(-=)    
var x = 10;
x -= 5; // same as x = x - 5
console.log(x)

// Multiplication Equal (*=) =>
var x = 10;
x *= 5; // same as x = x * 5
console.log(x)

// Division Equal (/=)
var x = 10;
x /= 5; // same as x = x / 5
console.log(x)

//Modulus Or Remainder Equal (%=)
var x = 10;
x %= 5; // same asx = x % 5
console.log(x)

//Power Or Exponent Equal (**=)
var x = 10;
x **= 3; // same as x = x % 5
console.log(x)

//Bitwise AND Equal (&=)
var x = 100;
x &= 5; // same as x = x & 5
console.log(x)

//Bitwise OR Equal (|=)
var x = 10;
x |= 3; 
console.log(x)

//Bitwise XOR Equal (^=)
var x = 10;
x ^= 3;
console.log(x)

//Bitwise left shift Equal (<<=)
var x = 10;
x >>= 3;
console.log(x)

//Bitwise Right shift Equal (>>=)
var x = 10;
x >>= 3;
console.log(x)

//Bitwise Zero fill right shift (>>> = )
var a = 8;
var  b = 1;
console.log( a >>>= b)